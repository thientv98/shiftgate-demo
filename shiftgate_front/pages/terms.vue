        <span
          style="font-size:14px"
        >対象自動車の売買契約について当社が代金決済代行サービスを履行した後は、当該売買契約が取消しまたは無効となったことにより、対象自動車または当該売買代金等を出品者および購入者との間で返還することになった場合であっても、当社はこれについて一切関与しないものとします。なお、当社は、事由により出品者および購入者との間の協議等を取り次ぐ場合がありますが、解決を保証するものではありません。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
