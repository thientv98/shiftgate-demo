      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
        <span style="font-family:Arial,Helvetica,sans-serif">
          <span style="font-size:14px">第37条（代金決済代行の委任）</span>
        </span>
      </strong>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、売買契約支援サービスを利用して売買が成立した対象自動車の売買代金の授受について、必ず当社に委託するものとします。</span>
      </span>
