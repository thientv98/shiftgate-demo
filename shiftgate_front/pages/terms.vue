          style="font-size:14px"
        >出品者は、当社が出品者に前項の金額を支払う際に、出品者が当社に対して支払義務を負うSHIFTGATE利用料、本手数料、オプションサービス料等の合計額を、当社の出品者に対する支払額と対当額において相殺し、相殺後の残額を当社から出品者に支払うことにあらかじめ承諾するものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >当社は、出品者が自動車引渡支援サービスを利用して対象自動車を当社または当社が指定する者に引き渡し、かつ、当社または当社が指定する者が購入者に対象自動車を引き渡した日から5営業日以内に、出品者に対し、前２項の規定に従い算出された金額を、出品者の指定する銀行口座に振り込む方法によって支払うものとします。なお、振込手数料は出品者が負担するものとします。</span>
      </span>
    </p>

