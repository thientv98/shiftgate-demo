      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、前項の規定に基づく当社への売却代金決済代行の委託を解除できないものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、対象自動車の売買代金を購入者から直接授受することはできないものとします。</span>
      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
        <span style="font-family:Arial,Helvetica,sans-serif">
          <span style="font-size:14px">第38条（対象自動車にかかる残債務の処理）</span>
