        <span>2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、前項に定める通知が行われた後速やかに、当社等との間で、対象自動車の引渡日時（売買契約成立時に合意した期限内に限ります）および場所について決定するものとします。</span>
      </span>
    </p>

