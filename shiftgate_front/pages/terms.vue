    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、当社が第１編第29条第2項の規定に基づいて行う通知を受領したときは、当該通知を受領した後５日以内に、当社等に対し、対象自動車を引き渡すものとします。</span>
