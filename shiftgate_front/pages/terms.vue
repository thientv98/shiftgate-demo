          style="font-size:14px"
        >対象自動車について第三者に対する担保権（所有権留保を含みます）が設定されている場合、出品者は、購入者がエスクロー口座に入金した対象自動車の売買代金をもって当該担保権の被担保債権にかかる債務を完済できるものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
