      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >出品者は、当社等から申入れがあった対象自動車の引渡日時について、合理的な理由がある場合を除き、これに応じるものとします。</span>
      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
        <span style="font-family:Arial,Helvetica,sans-serif">
          <span style="font-size:14px">第44条（購入者に対する対象自動車の引渡し）</span>
        </span>
      </strong>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >当社等は、出品者から対象自動車の引渡しを受けたときは、購入者に対し、 SHIFTGATEサービスが提供する機能を利用してその旨を通知するものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >当社等は、前項に定める通知が行われた後速やかに、対象自動車の引渡日時（売買契約成立時に合意した期限内に限ります）および場所について決定するものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >購入者は、当社等から申入れがあった対象自動車の引渡日時について、合理的な理由がある場合を除き、これに応じるものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >当社等は、購入者に対して対象自動車を引き渡したときは、出品者に対し、 SHIFTGATEサービスが提供する機能を利用してその旨通知するものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >購入者は、対象自動車の引渡しを受けた後３日以内に対象自動車の状態を確認するものとします。</span>
      </span>
    </p>

    <p>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span>5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
        <span
          style="font-size:14px"
        >対象自動車に、故障、瑕疵、欠陥、不足その他の不具合がある場合であっても、その責任は、出品者と購入者との間の売買契約に基づき解決されるものであり、当社はこれについて一切関与しないものとします。</span>
      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
