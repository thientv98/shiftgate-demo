      </span>
      <span style="font-family:Arial,Helvetica,sans-serif">
        <span style="font-size:14px"></span>
      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
        <span style="font-family:Arial,Helvetica,sans-serif">
          <span style="font-size:14px">第43条（出品者から当社への対象自動車の引渡し）</span>
