    <p>
      <span class="text-size-2">
        <strong>
          <span style="font-family:Arial,Helvetica,sans-serif">第4章&nbsp;&nbsp;&nbsp; 代金決済代行サービス</span>
        </strong>
      </span>
    </p>

    <p>
