      </span>
    </p>

    <span class="endline"></span>

    <p>
      <strong>
        <span style="font-family:Arial,Helvetica,sans-serif">
          <span style="font-size:14px">第39条（対象自動車の売買代金等の精算および出品者によるSHIFTGATE利用料の支払い）</span>
        </span>
      </strong>
    </p>

    <p>
